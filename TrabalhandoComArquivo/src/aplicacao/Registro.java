package aplicacao;

public class Registro {
	
	private String nome;
	private String id;
	private String bruxo;
	
	public String getBruxo() {
		return bruxo;
	}
	public void setBruxo(String bruxo) {
		this.bruxo = bruxo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	

}
